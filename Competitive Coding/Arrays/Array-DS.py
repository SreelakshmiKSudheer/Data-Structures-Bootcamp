def reverseArray(a):
    """
    Reverses the elements of the input array.

    Args:
        a (list): The array (list) of elements to be reversed.

    Returns:
        list: A new list containing the elements of 'a' in reverse order.

    Example:
        >>> reverseArray([1, 2, 3, 4])
        [4, 3, 2, 1]
    """
    # Write your code here
    return a[::-1]